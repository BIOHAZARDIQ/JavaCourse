package org.syntax;

/**
 * Enum is a special "class" that represents a group of constants (unchangeable variables, like final variables).
 * This is an example of advanced enum, that contains list of all Keywords of Java programming language.
 * For simple example of enum, @see class {@link Enums}.
 * Each enum item contains name, description, and in most cases link to class that is demonstrating mentioned keyword.
 */
public enum JavaKeywordsEnum {

    ABSTRACT("A non-access modifier. Used for classes and methods: An abstract class cannot be used to create objects (to access it, it must be inherited from another class). An abstract method can only be used in an abstract class, and it does not have a body. The body is provided by the subclass (inherited from)"),
    ASSERT("For debugging"),
    BOOLEAN("A data type that can only store true and false values"),
    BREAK("Breaks out of a loop or a switch block"),                    /** {@link Loops} */
    BYTE("A data type that can store whole numbers from -128 and 127"), /** {@link Variables} */
    CASE("Marks a block of code in switch statements"),                 /** {@link Switch} */
    CATCH("Catches exceptions generated by try statements"),            /** {@link Exceptions} */
    CHAR("A data type that is used to store a single character"),       /** {@link Variables} */
    CLASS("Defines a class"),                                           /** {@link org.abstraction.OOPExample} */
    CONTINUE("Continues to the next iteration of a loop"),              /** {@link Loops} */
    CONST("Defines a constant. Not in use - use final instead"),
    DEFAULT("Specifies the default block of code in a switch statement"),         /** {@link Switch} */
    DO("Used together with while to create a do-while loop"),                     /** {@link Loops} */
    DOUBLE("A data type that can store whole numbers from 1.7*10^308 to 1.7*10^308"), /** {@link Variables} */
    ELSE("Used in conditional statements"),
    ENUM("Declares an enumerated (unchangeable) type"),                               /** {@link Enums} or this class */
    EXPORTS("Exports a package with a module. New in Java 9"),
    EXTENDS("Extends a class (indicates that a class is inherited from another class)"), /** {@link org.inheritance.InheritanceExample} */
    FINAL("A non-access modifier used for classes, attributes and methods, which makes them non-changeable (impossible to inherit or override)"),
    FINALLY("Used with exceptions, a block of code that will be executed no matter if there is an exception or not"), /** {@link Exceptions} */
    FLOAT("A data type that can store whole numbers from 3.4*10^038 to 3.4*10^038"),         /** {@link Variables} */
    FOR("Create a for loop"),                 /** {@link Loops} */
    GOTO("Not in use, and has no function"),
    IF("Makes a conditional statement"),      /** {@link Operators} */
    IMPLEMENTS("Implements an interface"),    /** {@link org.abstraction.AbstractionExample} */
    IMPORT("Used to import a package, class or interface"),
    INSTANCEOF("Checks whether an object is an instance of a specific class or an interface"),
    INT("A data type that can store whole numbers from -2147483648 to 2147483647"),                    /** {@link Variables} */
    INTERFACE("Used to declare a special type of class that only contains abstract methods"),          /** {@link org.abstraction.InterfaceExample} */
    LONG("A data type that can store whole numbers from -9223372036854775808 to 9223372036854775808"), /** {@link Variables} */
    MODULE("Declares a module. New in Java 9"),
    NATIVE("Specifies that a method is not implemented in the same Java source file (but in another language)"),
    NEW("Creates new objects"),       /** {@link org.abstraction.OOPExample} */
    PACKAGE("Declares a package"),    /** {@link org.packages.PackagesExample1} */
    PRIVATE("An access modifier used for attributes, methods and constructors, making them only accessible within the declared class"), /** {@link org.packages.packageC.AccessModifiersClass} */
    PROTECTED("An access modifier used for attributes, methods and constructors, making them accessible in the same package and subclasses"), /** {@link org.packages.packageC.AccessModifiersClass} */
    PUBLIC("An access modifier used for classes, attributes, methods and constructors, making them accessible by any other class"), /** {@link org.packages.packageC.AccessModifiersClass} */
    REQUIRES("Specifies required libraries inside a module. New in Java 9"),
    RETURN("Finished the execution of a method, and can be used to return a value from a method"), /** {@link org.abstraction.MethodExamples} */
    SHORT("A data type that can store whole numbers from -32768 to 32767"),                        /** {@link Variables} */
    STATIC("A non-access modifier used for methods and attributes. Static methods/attributes can be accessed without creating an object of a class"), /** {@link org.abstraction.StaticClassExample} */
    STRICTFP("Restrict the precision and rounding of floating point calculations"),
    SUPER("Refers to superclass (parent) objects"),                 /** {@link org.inheritance.InheritanceExample} and {@link org.inheritance.Car} */
    SWITCH("Selects one of many code blocks to be executed"),       /** {@link Switch} */
    SYNCHRONIZED("A non-access modifier, which specifies that methods can only be accessed by one thread at a time"),
    THIS("Refers to the current object in a method or constructor"),/** {@link org.abstraction.OOP} */
    THROW("Creates a custom error"),                                /** {@link Exceptions} */
    THROWS("Indicates what exceptions may be thrown by a method"),  /** {@link Exceptions} */
    TRANSIENT("A non-accesss modifier, which specifies that an attribute is not part of an object's persistent state"),
    TRY("Creates a try...catch statement"),                         /** {@link Exceptions} */
    VAR("Declares a variable. New in Java 10"),
    VOID("Specifies that a method should not have a return value"), /** {@link org.abstraction.MethodExamples} */
    VOLATILE("Indicates that an attribute is not cached thread-locally, and is always read from the \"main memory\""),
    WHILE("Creates a while loop");                                  /** {@link Loops} */

    public final String description;

    JavaKeywordsEnum(String description) {
        this.description = description;
    }

    public static void main(String... args) {
        for (JavaKeywordsEnum oneKeyword : JavaKeywordsEnum.values()) {   //process all values of enums
            System.out.println(oneKeyword.name() + " - " + oneKeyword.description);
        }
    }
}
